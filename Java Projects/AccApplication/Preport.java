    void repo(){
        DefaultTableModel model = (DefaultTableModel)ptable.getModel();
        try{
            Statement st = con1.createStatement();
            ResultSet rs = st.executeQuery("select * from purchase");
            while(rs.next()){
                String ID = String.valueOf(rs.getInt("ID"));
                String PurchaseNo = rs.getString("InvoiceNo");
                String item = rs.getString("item");
                String Company = rs.getString("CompanyName");
                String City = rs.getString("City");
                String Qty = String.valueOf(rs.getInt("Qty"));
                String Pprice = String.valueOf(rs.getInt("Pprice"));
                String Pdate = rs.getString("Pdate");
                String Totalamt = String.valueOf(rs.getInt("Totalamt"));

                String tbData[] = {ID,PurchaseNo,item,Company,City,Qty,Pprice,Pdate,Totalamt };
                DefaultTableModel tmodel = (DefaultTableModel)ptable.getModel();
                tmodel.addRow(tbData);
                
            }
        }catch(Exception ee){
            System.out.println(ee);
               
        }
    }

