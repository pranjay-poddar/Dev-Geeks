    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        if(roleCb.getSelectedItem().toString().equals("Seller")){
        String query = "select * from Supermarket.Seller where Sel_Name = '"+userId.getText()+"' and Sel_Pssd = '"+password.getText()+"'";
        try{
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/Supermarket","root","root");
            st = con.createStatement();
            rs = st.executeQuery(query);
            
            if(rs.next()){
                new SellerInterface().setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(this,"Invalid Seller Credentials");
            }
        }
        catch(Exception e){
            e.printStackTrace();
            }
        }
        else{
        String query = "select * from Supermarket.Admin where Admin_Name = '"+userId.getText()+"' and Admin_Pssd = '"+password.getText()+"'";
        try{
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/Supermarket","root","root");
            st = con.createStatement();
            rs = st.executeQuery(query);
            
            if(rs.next()){
                new Products().setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(this,"Invalid Admin Credentials");
            }
        }
        catch(Exception e){
            e.printStackTrace();
            }
        }
    }//GEN-LAST:event_loginBtnActionPerformed

